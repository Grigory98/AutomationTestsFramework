package dto;

public class Car {
    private int id;
    private String engineType;
    private String mark;
    private String model;
    private double price;

    public int getId() {
        return id;
    }

    public String getEngineType() {
        return engineType;
    }

    public String getMark() {
        return mark;
    }

    public String getModel() {
        return model;
    }

    public double getPrice() {
        return price;
    }
}
