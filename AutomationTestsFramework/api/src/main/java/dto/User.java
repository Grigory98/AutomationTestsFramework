package dto;

public class User {
    private int id;
    private String firstName;
    private String secondName;
    private int age;
    private String sex;
    private double money;

    public int getId() {
        return id;
    }

    public String getFirstName() {
        return firstName;
    }

    public String getSecondName() {
        return secondName;
    }

    public int getAge() {
        return age;
    }

    public String getSex() {
        return sex;
    }

    public double getMoney() {
        return money;
    }
}
